public class User {
    private int ID;
    private String Name;
    private int Money;

    /** ID */
    public void setID(int id) {
        this.ID = id;
    }

    public int getID() {
        return ID;
    }

    /** Name */
    public void setName(String name) {
        this.Name = name;
    }

    public String getName() {
        return Name;
    }

    /** Money */
    public void setMoney(int money) {
        this.Money = money;
    }

    public int getMoney() {
        return Money;
    }
}
